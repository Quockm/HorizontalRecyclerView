package com.example.horizontalrecyclerview;

public class Monan1 {
    Integer hinnhmonan1;
    String tenmonan1, diachi1, khuyenmai1;

    public Monan1(Integer hinnhmonan1, String tenmonan1, String diachi1, String khuyenmai1) {

        this.hinnhmonan1 = hinnhmonan1;
        this.tenmonan1 = tenmonan1;
        this.diachi1 = diachi1;
        this.khuyenmai1 = khuyenmai1;
    }

    public Integer getHinnhmonan1() {
        return hinnhmonan1;
    }

    public String getTenmonan1() {
        return tenmonan1;
    }

    public String getDiachi1() {
        return diachi1;
    }

    public String getKhuyenmai1() {
        return khuyenmai1;
    }
}
